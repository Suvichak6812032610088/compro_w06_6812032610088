#include <stdio.h>

int main() {
    // กำหนดค่าเริ่มต้น
    int a = 5, b = 2;
    float x = 3.0, y = 4.5;

    // 1 r1: ใช้ a++ จะใช้ค่า a = 5 ก่อน แล้วจะเพิ่ม a เป็น 6
    // คำนวณ: r1 = 5 * 2 + ((int)3.0 % 3) = 10 + 0 = 10
    int r1 = a++ * b + (int)x % 3;

    // 2 r2: a = 6 (หลังจากเพิ่มแล้ว), b = 2, x = 3.0
    // คำนวณ: (6 > 2) && ((int)3.0 / 2 < 2)
    // → (true) && (1 < 2) → true → 1
    int r2 = (a > b) && ((int)x / b < 2);

    // 3 r3: ++x จะกลายเป็น 4.0, a = 6, y = 4.5
    // คำนวณ: r3 = 4.0 * 4.5 - 6 / 2 = 18.0 - 3 = 15.0
    float r3 = ++x * y - a / 2;

    // 4 r4: (x += 1.5) → x = 5.5 เปรียบเทียบกับ y = 4.5 → (5.5 > 4.5) = true
    // b = 2, (b-- > 0) = true และ b กลายเป็น 1
    // → r4 = true && true → 1.00
    float r4 = ((x += 1.5) > y) && (b-- > 0);

    // แสดงผลลัพธ์โดย float แสดงทศนิยม 2 ตำแหน่ง
    printf("r1 = %d\n", r1);
    printf("r2 = %d\n", r2);
    printf("r3 = %.2f\n", r3);
    printf("r4 = %.2f\n", r4);

    return 0;
}
